  999 RED=0.D0
      RETURN
      END
C*DECK RACAH
      FUNCTION RACAH(JA,JB,JC,JD,JE,JF)
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION G(121),I(16)
      COMMON/RACCM/G
      EQUIVALENCE(I(1),I1),(I(2),I2),(I(3),I3),(I(4),I4),(I(5),I5),
     1 (I(6),I6),(I(7),I7),(I(8),I8),(I(9),I9),(I(10),I10),(I(11),I11),
     2 (I(12),I12),(I(13),I13),(I(14),I14),(I(15),I15),(I(16),I16)
      DATA IT/0/
      IM=101
      RACAH=0.D0
      IF(IT-20431)10,30,10
C     FORM FACTORIAL TABLE ON FIRST PASS
   10 IT=20431
      G(1)=0.D0
      G(2)=0.D0
      DO 20 J=3,IM
      X=J-1
      X=J-1
   20 G(J)=G(J-1)+DLOG(X)
C     CONVERT ARGUMENTS TO INTEGER AND MAKE USEFUL COMBINATIONS
   30 I1=JA+JB-JE
      I2=JB+JE-JA
      I3=JE+JA-JB
      I4=JC+JD-JE
      I5=JD+JE-JC
      I6=JE+JC-JD
      I7=JA+JC-JF
      I8=JC+JF-JA
      I9=JF+JA-JC
      I10=JB+JD-JF
      I11=JD+JF-JB
      I12=JF+JB-JD
      I13=JA+JB+JE
      I14=JC+JD+JE
      I15=JA+JC+JF
      I16=JB+JD+JF
C     CHECK TRIANGULAR INEQUALITIES,FIND NO. OF TERMS IN SUM,DIV. I BY 2
      N=I16
      DO 80 J=1,12
      K=I(J)
   40 IF(K)300,50,50
   50 IF(K-N)60,70,70
   60 N=K
   70 I(J)=K+1
   80 CONTINUE
C     FIND MINIMUM VALUE OF SUMMATION INDEX
      IL=0
      DO 100 J=13,16
      IF(IL-I(J))90,100,100
   90 IL=I(J)
  100 CONTINUE
      J1=IL-I13+1
      J2=IL-I14+1
      J3=IL-I15+1
      J4=IL-I16+1
      J5=I13+I4-IL
      J6=I15+I5-IL
      J7=I16+I6-IL
      H=-(-1.D0)**J5*EXP ((G(I1)+G(I2)+G(I3)-G(I13+2)+G(I4)+G(I5)+G(I6)-
     1G(I14+2)+G(I7)+G(I8)+G(I9)-G(I15+2)+G(I10)+G(I11)+G(I12)-G(I16+2))
     2/2.D0+G(IL+2)-G(J1)-G(J2)-G(J3)-G(J4)-G(J5)-G(J6)-G(J7))
      IF(N)300,110,120
  110 RACAH=H
      GO TO 300
  120 S=1.D0
      Q=N-1
      P=IL+2
      R=J1
      U=J2
      V=J3
      W=J4
      X=J5-1
      Y=J6-1
      Z=J7-1
      DO 130 J=1,N
      T=(P+Q)/(R+Q)*(X-Q)/(U+Q)*(Y-Q)/(V+Q)*(Z-Q)/(W+Q)
      S=1.D0-S*T
      Q=Q-1.D0
  130 CONTINUE
      RACAH=H*S
  300 RETURN
      END
