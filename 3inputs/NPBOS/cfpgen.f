      K=ND3*(ND3-1)/2+1+LPR+IBDEL(2,NCP)
      RED=RED*REDGS(K,LD)
      RETURN
C
  200 CONTINUE
C      NBD=1
C
      IF(NCD) 211,210,999
  211 IF(NCD+1) 999,220,999
C
  210 CONTINUE
C      NCD=0
C
      IF(NC.LT.0) GOTO 999
      ND3=ND-2*NB-3*NC-1
      IF(ND3) 999,212,212
  212 IF(LPR.LT.ND3) GOTO 999
