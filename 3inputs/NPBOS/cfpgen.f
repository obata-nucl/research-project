C*DECK CBANRM
      FUNCTION CBANRM(T,ND,L,REDGS,DIM)
C           Question: Is the argument variable "T" used anywhere?
      IMPLICIT REAL*8(A-H,O-Z)
      INTEGER T,DIM
      DIMENSION REDGS(DIM,5)
      CBANRM=0.D0
      K1=(ND-3)*(ND-4)/2+1
      K2=(ND-4)*(ND-5)/2+1
      L2PI=L-1
      L2PF=L+3
      K=ND-3
      IF(L2PI.LT.K) L2PI=K
      K=2*K-1
      IF(L2PF.GT.K) L2PF=K
      L1PI=L-1
      L1PF=L+3
      K=ND-2
      IF(L1PI.LT.K) L1PI=K
      K=2*K-1
      IF(L1PF.GT.K) L1PF=K
      DO 1 L2P=L2PI,L2PF
      L2=L2P-1
      LD20=L2-L+3
      CB=0.D0
      DO 2 L1P=L1PI,L1PF
      L1=L1P-1
      LD10=L1-L+3
      LD21=L2-L1+3
      IF(LD21.LT.1) GOTO 3
      IF(LD21.GT.5) GOTO 2
      CB=CB+RACAH(L,L2,2,2,2,L1)*REDGS(K1+L1,LD10)*REDGS(K2+L2,LD21)
    2 CONTINUE
    3 CONTINUE
      CBANRM=CBANRM+CB*REDGS(K2+L2,LD20)
    1 CONTINUE
      CBANRM=CBANRM*3/(2*L+1)
      RETURN
      END
C*DECK CBNNRM
      FUNCTION CBNNRM(T,ND,L,REDGS,DIM)
      IMPLICIT REAL*8(A-H,O-Z)
      INTEGER T,DIM
      DIMENSION REDGS(DIM,5)
      CBNNRM=0.D0
      L1PI=L-1
      L1PF=L+3
      K=ND-1
      IF(L1PI.LT.K) L1PI=K
      LT=2*ND-2-T
      K1=(ND-2)*(ND-3)/2+1
      K=(ND-3)*(ND-4)/2+1+L
      DO 1 L1P=L1PI,L1PF
      L1=L1P-1
      LDT1=L1-LT+3
      IF(LDT1.LT.1) GOTO 1
      IF(LDT1.GT.5) GOTO 2
      LD10=L-L1+3
      CBNNRM=CBNNRM+RACAH(L,2,LT,2,L1,2)*REDGS(K1+L1,LDT1)*REDGS(K,LD10)
    1 CONTINUE
    2 CONTINUE
      CBNNRM=CBNNRM*(-1)**T/SQRT(2.D0*L+1.D0)*3
      RETURN
      END
C*DECK DDDAG
      SUBROUTINE DDDAG(L,ND,NCL,LL,NCR,LR,ANS)
C      NCR .GE.NCL
C
C      ANS=THREEJ(L,2,LL,-M,DL,ML)*THREEJ(L,2,LR,-M,DR,MR)*
C           <ND,0,NCL,LL,ML/D*D+/ND,0,NCR,LR,MR>
C
      IMPLICIT REAL*8(A-H,O-Z)
      ANS=0.D0
      IF(LL.EQ.LR.AND.NCL.EQ.NCR) ANS=1.D0
      CALL DDHELP(AN1,ND,NCR,NCL,LR,LL,0,L)
      ANS=ANS+AN1
      IF(NCL-NCR) 99,3,99
    3 CONTINUE
      IF(NCR) 99,99,2
    2 CALL DDHELP(AN1,ND,NCR,NCL,LR,LL,1,L)
      ANS=ANS+AN1
   99 CONTINUE
      RETURN
      END
C*DECK DDHELP
      SUBROUTINE DDHELP(ANS,ND,NCR,NCL,LR,LL,NCDL,L)
C
C      CALCULATES PART OF <ND,0,NCL,LL//D+*D//ND,0,NCR,LR>
C       WITH INTERMEDIATE STATE SPECIFIED BY NCDL
C      WAY OF COPLING : (LL,2)L  AND (LR,2)L
C
      IMPLICIT REAL*8(A-H,O-Z)
      ANS=0.D0
      NC=NCL-NCDL
      K=ND-3*NC-1
      NCDR=NCR-NC
      L1PI=K+1
      L1PF=2*K+1
      L1=LR-1
      IF(L1.GT.L1PI) L1PI=L1
      L1=LL-1
      IF(L1.GT.L1PI) L1PI=L1
      IF(L1PI.GT.L1PF) RETURN
      DO 1 L1P=L1PI,L1PF
      L1=L1P-1
      LDL=L1-LL+3
      LDR=L1-LR+3
      IF(LDL.GT.5) GOTO 2
      IF(LDR.GT.5) GOTO 2
      ANS=ANS+RACAH(2,LR,LL,2,L1,L)*RED(ND-1,0,NC,L1,LDL,0,NCDL)*
     *        RED(ND-1,0,NC,L1,LDR,0,NCDR)
    1 CONTINUE
    2 CONTINUE
      RETURN
      END
C*DECK CFPTST
      SUBROUTINE CFPTST(NDM)
      IMPLICIT REAL*8(A-H,O-Z)
      REAL KLD
      DIMENSION KLD(5),CFP(5)
      DO 1 ND=1,NDM
      NBPM=ND/2+1
      DO 2 NBP=1,NBPM
      NB=NBP-1
      NCPM=(ND-2*NB)/3+1
      DO 3 NCP=1,NCPM
      NC=NCP-1
      LPI=ND-2*NB-3*NC+1
      LPF=2*LPI-1
      DO 4 LP=LPI,LPF
      L=LP-1
      IF(L.LE.7) GOTO 16
      IF(ND.LE.3) GOTO 16
      IF(L.GT.2*(ND-3)) GOTO 16
      LI=L-2
      LF=L+2
      T=0
      DO 15 L1=LI,LF
      L1L=L1-L+3
      DO 14 L2=LI,LF
      L21=L2-L1+3
      LD2=L-L2+3
      T13=0.D0
      IF(L21.LT.1) GOTO 14
      IF(L21.GT.5) GOTO 15
      DO 13 NBD1P=1,2
      NBD1=NBD1P-1
      NB1=NB-NBD1
      DO 13 NBC1=1,2
      NCD1=NBC1-NBD1P
      NC1=NC-NCD1
      T12=0.D0
      DO 12 NB2P=1,2
      NB2=NB2P-1
      DO 12 NC2P=1,2
      NC2=NC2P-1
      NC12=NC1-NC2
   12 T12=T12+RED(ND-3,0,0,L,LD2,NB2,NC2)*
     *        RED(ND-2,NB2,NC2,L2,L21,NB12,NC12)
   13 T13=T13+T12*RED(ND-1,NB1,NC1,L1,L1L,NBD1,NCD1)
   14 T=T+T13*RACAH(L,L2,2,2,2,L1)*(-1)**(L+L2)
   15 CONTINUE
      S=0
      DO 25 L1=LI,LF
      L1D=L1-L+3
      LD1=L-L1+3
      S22=0.D0
      DO 22 NB1P=1,2
      NB1=NB1P-1
      NBD=NB-NB1
      DO 22 NC1P=1,2
      NC1=NC1P-1
      NCD=NC-NC1
   22 S22=S22+RED(ND-2,0,0,L,LD1,NB1,NC1)*RED(ND-1,NB1,NC1,L1,L1D,NBD,
     &  NCD)
   25 S=S+S22*(-1)**L1
      S=S/(4*L+2.D0)
      T=T/(2*L+1.D0)
      WRITE(2,234) ND,NB,NC,L,S,T
  234 FORMAT(30X,4I4,2F12.7)
   16 CONTINUE
      CF=0.D0
      DO 5 LD=1,5
      LR=LD+L-3
      DO 6 NBDP=1,2
      NBD=NBDP-1
      NBR=NB-NBD
      IF(NBR) 6,61,61
   61 DO 7 NCDP=1,3
      NCD=NCDP-2
      NCR=NC-NCD
      IF(NCR) 7,71,71
   71 CF=CF+(RED(ND-1,NBR,NCR,LR,LD,NBD,NCD))**2
    7 CONTINUE
    6 CONTINUE
    5 CONTINUE
      SUM=ND*(2*L+1)
      LBD=ND-2*NB-3*NC
      IF(L.EQ.(2*LBD-1)) SUM=0.D0
      DIF=ABS(CF-SUM)
      IF(DIF.LT.0.00001D0) GOTO 4
      WRITE(2,200) ND,NB,NC,L,CF
  200 FORMAT(4X,4I4,F12.7)
    4 CONTINUE
    3 CONTINUE
    2 CONTINUE
    1 CONTINUE
      DO 10 ND=1,NDM
      NBPM=ND/2+1
      DO 10 NBP=1,NBPM
      NB=NBP-1
      ND2=ND-2*NB
      NCPM=ND2/3+1
      DO 10 NCP=1,NCPM
      NC=NCP-1
      ND3=ND2-3*NC
      LPI=ND3+1
      LPF=2*ND3+1
      DO 10 LP=LPI,LPF
      L=LP-1
      IF(LP.EQ.(LPF-1)) GOTO 11
      FAC=SQRT((2.D0*L+1.D0)*ND)
      WRITE(2,201)
  201 FORMAT(1X)
C
      DO 20 NBDP=1,2
      NBD=NBDP-1
      NBR=NB-NBD
      IF(NBR) 20,21,21
   21 DO 30 NBC=1,2
      NCD=NBC-NBDP
      NCR=NC-NCD
      IF(NCR) 30,31,31
   31 NDR3=ND-1-2*NBR-3*NCR
      IF(NDR3) 30,32,32
   32 IF(L+2.LT.NDR3) GOTO 30
      IF(L-2.GT.2*NDR3) GOTO 30
      DO 40 LD=1,5
      LR=LD+L-3
      KLD(LD)=RED(ND-1,NBR,NCR,LR,LD,NBD,NCD)
      CFP(LD)=KLD(LD)/FAC
   40 CONTINUE
