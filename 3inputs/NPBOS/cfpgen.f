C
C  PROGRAM CFPGEN GENERATES  D-BOSON CFP.
C
C  NO INPUT DATA ARE ASSUMED.
C
C*DECK CFPGEN
      PROGRAM CFPGEN
C     (INPUT,OUTPUT,TAPE3,TAPE2=OUTPUT,TAPE1=INPUT)
C      SEE FOR FORMULAS FOR THE DIMENSIONS PROG. PHINT
      IMPLICIT REAL*8(A-H,O-Z)
      COMMON/REDMAT/REDGS( 546,5),BETFAC( 72,2),IBDEL(2,6)
      COMMON/RACCM/G(121)
      DATA NDM,IDIB,IDB,IDGS/16,6,72,546/
      OPEN(1,STATUS='SCRATCH',FORM='UNFORMATTED',ACCESS='SEQUENTIAL')
      OPEN(2,FILE='out1.dat',STATUS='UNKNOWN',FORM='FORMATTED',
     &     ACCESS='SEQUENTIAL')
      OPEN(UNIT=3,FILE='cfp1.dat',STATUS='UNKNOWN',FORM='UNFORMATTED',
     &     ACCESS='SEQUENTIAL')
      NPHMSU=NDM
      CALL GENBTA(BETFAC,IDB,NPHMSU)
      CALL IBGEN(IBDEL,IDIB,NPHMSU,IDGS)
      CALL GENGS(REDGS,IDGS,NPHMSU)
      CALL NCGEN(REDGS,IDGS,NPHMSU,IBDEL,IDIB)
      CALL CFPTST(NPHMSU)
      CALL WRTRED(NDM,IDGS,IDB,IDIB,REDGS,BETFAC,IBDEL)
      close (3)
      STOP
      END
C*DECK IBGEN
      SUBROUTINE IBGEN(IBDEL,IDIM,NDMAX,IDGS)
C
C      IBDEL(NCD+1,NCMIN+1)
C      Gives base line for this sort of RED. MAT. IN
C      ARRAY  REDGS
C
      IMPLICIT REAL*8(A-H,O-Z)
      CHARACTER*10 IBD,IGS
      DIMENSION IBDEL(2,IDIM)
      DATA IBD,IGS/ 'IBDEL     ' , 'REDGS     ' /
      IDM=NDMAX/3+1
      IF(IDM.GT.IDIM) GOTO 99
      IBDEL(1,1)=0
      IBDEL(2,1)=NDMAX*(NDMAX+1)/2
      DO 1 I=2,IDM
      IBDEL(1,I)=IBDEL(2,I-1)+(NDMAX+4-3*I)*(NDMAX+5-3*I)/2
      IBDEL(2,I)=IBDEL(1,I)+(NDMAX+3-3*I)*(NDMAX+4-3*I)/2
    1 CONTINUE
      IDM=IBDEL(2,IDM)
      IF(IDM.GT.IDGS) CALL DIMER(IGS,IDM)
      RETURN
   99 CALL DIMER(IBD,IDM)
      RETURN
      END
C
C*DECK GENBTA
C
      subroutine GENBTA(BETFAC,DIM,NDM)
C
C      <ND+1,NBPR,NCPR,IPR//D+//ND,NB,NC,I>=
C      =BETFAC(J,L)*<ND+1--2*NBPR,0,NCPR,IPR//(D+ OR D)//ND-2*NB,0,NC,I>
C      J=(K+1)*(ND-K)+1+NB  WITH K=ND/2
C      L=NBPR-NB+1
C      DIMBET=(K+1)*(NDM-1-K)+1+K  WITH K=(NDM-1)/2
C
C      ND : # of d-bosons
C      NB : n_{\beta}=(ND-v)/2, seniority
C      NC : n_{\Delta}, # of boson triplets coupled to zero angular momentum
C
      IMPLICIT REAL*8(A-H,O-Z)
      INTEGER DIM
      DIMENSION BETFAC(DIM,2)
      NDMM=NDM-1
      K=NDMM/2
      IDIM=K+1+(K+1)*(NDMM-K)
      IF(IDIM.GT.DIM) GOTO 99
      DO 1 K=1,IDIM
      DO 1 I=1,2
    1 BETFAC(K,I)=1.D0
      DO 2 ND=1,NDMM
      K=ND/2
      I=1+(K+1)*(ND-K)
      KP=K+1
      DO 3 NBP=1,KP
      NB=NBP-1
      NDB=ND-2*NB
      BETFAC(I+NB,1)=SQRT(CV(NDB+1,NB,NB)/CV(NDB,NB,NB))
      BETFAC(I+NB,2)=CV(NDB-1,NBP,NB)/SQRT(CV(NDB-1,NBP,NBP)*CV(NDB,NB,N
     &B))
    3 CONTINUE
    2 CONTINUE
      RETURN
   99 WRITE(2,200)  IDIM,DIM
  200 FORMAT(10X,'GENBTA : ',I5,' GT ',I5)
      RETURN
      END
C*DECK CV
      FUNCTION CV(ND,NBP,NB)
C      NBP .GE. NB
      IMPLICIT REAL*8(A-H,O-Z)
      CV=1.D0
      IF(NB.EQ.0) RETURN
      DO 1 KP=1,NB
    1 CV=CV*(NBP-KP+1)*(ND+NBP-KP+2.5D0)
      RETURN
      END
C*DECK GENGS
      SUBROUTINE GENGS(REDGS,DIM,NDM)
C
C      DIM=NDM*(NDM+1)/2
C      K=ND*(ND-1)/2+1+LR
C      LD==LR-LL+3
C       REDGS(K,LD)=<ND+1,LL//D+//ND,LR>  NB=NC=0   ; ND<=L<=2*ND
C
      IMPLICIT REAL*8(A-H,O-Z)
      INTEGER T,TP,DIM
      LOGICAL FTIME
      DIMENSION REDGS(DIM,5)
      K=NDM*(NDM+1)/2
      CAN=0.D0
      CBN=0.D0
      CBB=0.D0
      CBA=0.D0
      CAA=0.D0
      DO 1 ND=1,DIM
      DO 1 LDELT=1,5
    1 REDGS(ND,LDELT)=0.D0
C
C      FILL IN STRECHED CASES
C
      DO 2 ND=1,NDM
      K=ND*(ND+1)/2
    2 REDGS(K,1)=SQRT(DFLOAT(ND*(4*ND+1)))
      DO 3 ND=2,NDM
      L2MAX=2*(ND-2)
      L3MAX=L2MAX-2
      L4MAX=2*(ND-4)
      L5MAX=2*(ND-5)
      KS=(ND-1)*(ND-2)/2-1+2*ND
      LM=2*ND
      DO 4 L=ND,LM
      IF(L.EQ.(LM-1)) GOTO 4
      FTIME=.TRUE.
      AFAC=0.D0
      BFAC=0.D0
      DO 5 LDELT=1,5
      LT=LDELT-3+L
      T=LM-2-LT
      IF(T) 4,30,31
   31 IF(T-1) 30,5,32
   32 IF(T-ND) 30,5,5
   30 KST=KS-T
      IF(L.GT.L2MAX) GOTO 11
C
C      STATE WITH (ND-1,NB.NE.0) POSSIBLE
C
      CAA=ND+0.5D0
      LD=6-LDELT
      K=(ND-2)*(ND-3)/2+1+L
      CAN=(-1)**T*REDGS(K,LD)/SQRT(2.D0*L+1.D0)
      IF(L.EQ.(L3MAX-1)) GOTO 12
      IF(L.LE.L3MAX) GOTO 13
C
   12 CONTINUE
C      NO STATE (ND-1,NC.NE.0) POSSIBLE
C
      AFAC=CAN/CAA
      GOTO 11
   13 IF(.NOT.FTIME) GOTO 33
      CBB=3*T0(ND-3,L)
C
C      STATE (ND-1,NB.NE.0,NC.NE.0) POSSIBLE
C
      IF(L.GT.L4MAX) GOTO 15
      RNUM=CV(ND-4,2,2)
      IF(L.GT.L5MAX) GOTO 16
      IF((L+1).EQ.L5MAX) GOTO 16
      RNUM=RNUM-(CV(ND-4,2,1)*CBANRM(T,ND-2,L,REDGS,DIM))**2/
     1   PPTERM(ND,L,REDGS,DIM)
   16 CONTINUE
      CBB=CBB-4*(CBANRM(T,ND-1,L,REDGS,DIM)**2)/RNUM
   15 CONTINUE
      CBA=CBANRM(T,ND,L,REDGS,DIM)
   33 CONTINUE
      CBN=CBNNRM(T,ND,L,REDGS,DIM)
      RNOEM=CAA*CBB-CBA*CBA
      AFAC=(CAN*CBB-CBA*CBN)/RNOEM
      BFAC=(CBN*CAA-CAN*CBA)/RNOEM
C  11 IF(FTIME) 40,50
   11 IF(.NOT.FTIME) GO TO 50
   40 FTIME=.FALSE.
C
C      FIRST TIME WITH THIS L AND ND :
C      SIGN IS FREE
C
      LR=LT
      CALL DDDAG(L,ND-1,0,LT,0,LR,CNN)
      RN=(-1)**L/SQRT(CNN-AFAC*CAN-BFAC*CBN)
      TP=T
      RN1=1/RN
      CDS=CAN
      CDT=CBN
      GOTO 60
   50 CONTINUE
C
C      NOT FIRST TIME WITH THIS L AND ND :
C       SIGNS ARE RELATED
C
      CALL DDDAG(L,ND-1,0,LT,0,LR,CDD)
      RN=RN1/(CDD-AFAC*CDS-BFAC*CDT)
   60 REDGS(KST,LDELT)=(-1)**L*SQRT(2.D0*L+1.D0)/RN
    5 CONTINUE
    4 CONTINUE
    3 CONTINUE
      RETURN
      END
C*DECK NCGEN
      SUBROUTINE NCGEN(REDGS,IDGS,NDMAX,IBDEL,IDIB)
      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION REDGS(IDGS,5),IBDEL(2,IDIB)
      NCPM=NDMAX/3+1
      DO 1 NCP=2,NCPM
      NC=NCP-1
      NDI=3*NC
      DO 2 ND=NDI,NDMAX
C
C      COMPUTES RED MAT EL , NB=0  ND,NC => ND-1,NC-NCD
C
      LPI=ND-NDI+1
      LPF=2*LPI-1
      DO 3 LP=LPI,LPF
      L=LP-1
      IF(LP.EQ.(LPF-1)) GOTO 3
      L0=L+2
      FAC=(-1)**L*SQRT(DFLOAT(L+LP))
      CCB=RED(ND-1,0,NC-1,L0,5,1,-1)*FAC/(2*L+1)
      CAB=FAC*RED(ND-1,0,NC-1,L0,5,1,0)/(2*L+1)
      CNB=RED(ND-1,0,NC-1,L0,5,0,0)*FAC/(2*L+1)
      CALL DDDAG(L,ND-1,NC-1,L0,NC-1,L0,ANS)
      B=ANS-CNB*CNB-CAB*CAB-CCB*CCB
      IF(B.LE.0) GOTO 9
      B=1/SQRT(B)
C
C      ncd=1
C
      LPRI=L-2
      K=ND+3-3*NC-1
      LPRM=2*K
      IF(LPRI.LT.K) LPRI=K
      NR=ND-3*NC
      KL=IBDEL(2,NC)+NR*(NR-1)/2+1+L
      DO 4 LPR=LPRI,LPRM
      LD=LPR-L+3
      IF(LD.GT.5) GOTO 44
      CALL DDDAG(L,ND-1,NC-1,L0,NC-1,LPR,CF)
      CF=CF*FAC
      CF=B*(CF-CNB*RED(ND-1,0,NC-1,LPR,LD,0,0)-CAB*RED(ND-1,0,NC-1,LPR,
     , LD,1,0)-CCB*RED(ND-1,0,NC-1,LPR,LD,1,-1))
      REDGS(KL,LD)=CF
    4 CONTINUE
   44 CONTINUE
C
C      ncd=0
C
      NR=ND-3*NC-1
      KL=IBDEL(1,NC+1)+NR*(NR-1)/2+1
      LPRPI=L-1
      K=ND-3*NC
      IF(K.LT.1) GOTO 55
      IF(LPRPI.LT.K) LPRPI=K
      LPRPM=2*K-1
      DO 5 LPRP=LPRPI,LPRPM
      LPR=LPRP-1
      LD=LPR-L+3
      IF(LD.GT.5) GOTO 55
      CALL DDDAG(L,ND-1,NC-1,L0,NC,LPR,CF)
      CF=CF*FAC
      CF=B*(CF-CAB*RED(ND-1,0,NC,LPR,LD,1,-1))
      REDGS(KL+LPR,LD)=CF
    5 CONTINUE
   55 CONTINUE
      GOTO 3
    9 WRITE(2,200) NC,ND,L,ANS,CNB,CAB
  200 FORMAT(' NCGEN ',3I4,3F10.5)
    3 CONTINUE
    2 CONTINUE
    1 CONTINUE
      END
C*DECK T0
      FUNCTION T0(ND,L)
      IMPLICIT REAL*8(A-H,O-Z)
      T0=3.D0/35.D0*(ND*(4*ND+2)-L*(L+1))+6.D0*ND/5.D0+2
      RETURN
      END
C*DECK PPTERM
      FUNCTION PPTERM(ND,L,REDGS,DIM)
      IMPLICIT REAL*8(A-H,O-Z)
      INTEGER DIM
      INTEGER T
      DIMENSION REDGS(DIM,5)
      PPTERM=(ND+0.5D0)*T0(ND-5,L)+6.D0/35.D0*(L*(L+1)+ND-5)
      LI=L-2
      L2F=L+2
      L1F=L2F
      K=(ND-6)*2
      IF(K.LT.L2F) L2F=K
      IF(LI.GT.L2F) GOTO 9
      K=(ND-7)*2
      IF(K.LT.L1F) L1F=K
      IF(LI.GT.L1F) GOTO 9
      DO 1 L1=LI,L1F
      L1D=L1-L+3
      C=0.D0
      DO 2 L2=LI,L2F
      L12=L1-L2+3
      L2D=L2-L+3
      IF(L12.LT.1) GOTO 3
      IF(L12.GT.5) GOTO 2
      C=C+RACAH(2,L1,2,L,L2,2)*RED(ND-7,0,0,L1,L12,0,0)*
     ;     RED(ND-6,0,0,L2,L2D,0,0)
    2 CONTINUE
    3 CONTINUE
      PPTERM=PPTERM+C*RED(ND-6,0,0,L1,L1D,0,0)*
     ;   CBANRM(T,ND-4,L1,REDGS,DIM)/SQRT(2*L1+1.D0)
    1 CONTINUE
    9 CONTINUE
      PPTERM=3*PPTERM
      WRITE(2,200) ND,L,PPTERM
  200 FORMAT(1X,2I4,F10.5)
      IF(PPTERM.EQ.0.) PPTERM=1.D0
      RETURN
      END
